#!/usr/bin/python
module_path = None
play_status = False

#------------------------------------------------------------------------------#
#        do nothing                                                            #
#------------------------------------------------------------------------------#
def init(path):
    global module_path
    module_path = path
    return()

#------------------------------------------------------------------------------#
#        report properies                                                      #
#------------------------------------------------------------------------------#
def get_properties():
    props = {}
    props['type']       = 'service'
    props['name']       = 'usb'
    props['short_name'] = 'USB'
    props['controls']   = True
    props['button']     = "&#xF2DD;"
    return(props)

#------------------------------------------------------------------------------#
#       report empty metadata                                                  #
#------------------------------------------------------------------------------#
def get_metadata():
    meta_data = {}
    meta_data['track']  = ''
    meta_data['album']  = ''
    meta_data['artist'] = ''
    meta_data['cover']  = '/images/black.png'
    return(meta_data)

#------------------------------------------------------------------------------#
#      tune to a station                                                       #
#------------------------------------------------------------------------------#
def play():
    global play_status
    play_status = True
    get_metadata()

#------------------------------------------------------------------------------#
#      do nothing                                                              #
#------------------------------------------------------------------------------#
def stop():
    global play_status
    play_status = False
    get_metadata()

#------------------------------------------------------------------------------#
#          always report false                                                 #
#------------------------------------------------------------------------------#
def get_play_status():
    return(play_status)

#------------------------------------------------------------------------------#
#      do nothing                                                              #
#------------------------------------------------------------------------------#
def next():
    get_metadata()

#------------------------------------------------------------------------------#
#      do nothing                                                              #
#------------------------------------------------------------------------------#
def prev():
    get_metadata()
